const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
  profile: {
    type: mongoose.Schema.ObjectId,
    ref: 'Profile',
  },
});

module.exports = mongoose.model('User', userSchema);
